#include "libft.h"

char	*ft_strrev(char *str)
{
	int i;
	int len;
	char tmp;

	i = 0;
	len = ft_strlen(str) - 1;
	while (len > i)
	{
		tmp = str[i];
		str[i] = str[len];
		str[len] = tmp;
		i++;
		len--;
	}
	return (str);
}

int main(void)
{
	char *stringa = "abcdef";
	printf("%s",ft_strrev(stringa));
	return 0;	
}